path = input("Куда направится витязь? (направо/налево/прямо): ").lower().strip()

if path == "направо":
    print("\nБогатырь выбрал путь направо.")
    print("Конь споткнулся и пал в бою.")
    print("Но витязь нашёл новую лошадь и продолжил путь!")
elif path == "налево":
    print("\nБогатырь выбрал путь налево.")
    print("Забыл витязь свой дом и семью.")
    print("Стал отшельником в дремучем лесу.")
elif path == "прямо":
    print("\nБогатырь выбрал прямой путь.")
    print("Нашёл он сокровища несметные!")
    print("Стал богачом, но не забыл о долге.")
else:
    print("\nНет такого пути! Витязь остался у камня.")
    print("Ждёт он, когда судьба сама укажет дорогу.")

print("\n" + "=" * 40)
print("Каков бы ни был выбор — это путь героя!")