#!/bin/bash

# Запрашиваем массу и рост
echo "Введите вашу массу в килограммах:"
read weight

echo "Введите ваш рост в метрах (например, 1.75):"
read height

# Проверяем, что введены числа
if ! [[ "$weight" =~ ^[0-9]+\.?[0-9]*$ ]] || ! [[ "$height" =~ ^[0-9]+\.?[0-9]*$ ]]; then
    echo "Ошибка: пожалуйста, введите корректные числа."
    exit 1
fi

# Проверяем, что рост не равен нулю (чтобы избежать деления на ноль)
if (( $(echo "$height == 0" | bc -l) )); then
    echo "Ошибка: рост не может быть нулём."
    exit 1
fi

# Рассчитываем BMI: weight / (height * height)
# Используем bc для работы с дробными числами
bmi=$(echo "scale=2; $weight / ($height * $height)" | bc)

# Округляем до целого числа (берём целую часть)
bmi_int=${bmi%.*}

# Выводим результат
echo "Ваш индекс массы тела (ИМТ): $bmi_int"

