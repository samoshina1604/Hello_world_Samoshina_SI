#!/bin/bash

# Функция проверки прав суперпользователя
check_root() {
    if [ $EUID -ne 0 ]; then
        echo "ПРЕДУПРЕЖДЕНИЕ: Скрипт должен быть запущен от имени суперпользователя (root)!"
        echo "Текущий EUID: $EUID"
        exit 1
    fi
    echo "Скрипт запущен от имени root (EUID: $EUID). Продолжаем..."
}

# Вызываем функцию проверки
check_root

# Основной код скрипта (выполнится только если запущен от root)
echo "Выполнение основной части скрипта..."
echo "Текущий пользователь: $USER"
echo "Дата: $(date)"

